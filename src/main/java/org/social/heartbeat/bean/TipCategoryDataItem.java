package org.social.heartbeat.bean;

public class TipCategoryDataItem {
	
	private int id;
	private int category_id;
	private String data_item_heading;
	private String data_item;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getCategory_id() {
		return category_id;
	}
	public void setCategory_id(int category_id) {
		this.category_id = category_id;
	}
	public String getData_item_heading() {
		return data_item_heading;
	}
	public void setData_item_heading(String data_item_heading) {
		this.data_item_heading = data_item_heading;
	}
	public String getData_item() {
		return data_item;
	}
	public void setData_item(String data_item) {
		this.data_item = data_item;
	}

}
